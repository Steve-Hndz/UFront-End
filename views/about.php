                <span>¿Cómo se obtiene el plasma convaleciente?</span>
                El plasma convaleciente se refiere al plasma recolectado de personas que hayan sido confirmadas con COVID-19, y se han recuperado de la infección. <br>
                Se obtiene mediante un procedimiento llamado plasmaféresis que consiste en la separación de la sangre completa del donador, extrayendo por separación, únicamente plasma del donador, el cual es rico en electrolitos, componentes químicos, factores de coagulación, agua y lo más importante inmunoglobulinas, que ha desarrollado el donador posterior a la infección por COVID-19. <br>
                El procedimiento es sencillo, seguro y con poca probabilidad de complicaciones para el donador, y es realizado por personal experto en el area.

